from turtle import*
import math
penup()
pencolor('#cf8f03')
setpos(-120,0)
pensize(10)
pendown()
left(30)
a = math.tan(45)
b = a*45
c = math.sqrt(b**2 + 45**2)
forward(c)
right(60)
forward(c)
right(120)
forward(c)
right(60)
forward(c)
penup()
pencolor('#0b2c3c')
setpos(-80,0)
pendown()
right(120)
forward(c)
right(60)
forward(c)
right(120)
forward(c)
right(60)
forward(c)
mainloop()